#Write a Python program to demonstrate the creation of variables and different data types.

a = 1
print(type(a))

b = 'Hello'
print(type(b))

c = 3.14
print(type(c))

d = [1,2,3]
print(type(d))

e = (1,2,3)
print(type(e))

f = {1,2,3}
print(type(f))

g = {
    'Name':'Raj',
    'City':'Rajkot'
}
print(type(g))

h = 1 + 2j
print(type(h))

i = True
