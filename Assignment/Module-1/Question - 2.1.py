# Ask the user for input in integer
number_value = int(input("Enter a number: "))

# Check if the number is even or odd
if number_value % 2 == 0:
    # Print result when number is even
    print(f"{number_value} is Even.")
else:
    # Print result when number is odd
    print(f"{number_value} is Odd.")
